                    <h1>Reporting</h1>

                    <h2>What does Reporting do?</h2>
                    <p>Whistler's Reporting feature enables you to swiftly document and publicize events you are witnessing. You can capture audio, photo or
                        video evidence and send it in just a few swipes to a large number of recipients: your fellow activists, human rights organizations,
                        journalists, etc. The report created by Whistler also includes a whole range of data to help recipients authenticate the evidence:
                        location, elevation, luminosity, surrounding WiFi networks and cell towers.</p>

                    <h2>How does it work?</h2>
                    <p>Your report is uploaded to Whistler servers and we send an email from report@whistlerapp.org to everyone in your list of recipients. The
                        email includes a link to your report. We do not mention anything about your identity (which we don't know anyway).</p>

                    <h2>Can anyone see my reports?</h2>
                    <p>
                        Before sending the report from the app, you decide whether it should be public or not. If it's public, anyone can see it on our website
                        <a href="https://whistlerapp.org/reports">here</a>. If it's private, only your recipients will be able to see it, by following the URL
                        they'll receive from Whistler.
                    </p>

                    <h2>How many recipients can I send my report to?</h2>
                    <p>As many as you'd like.</p>

